package com.example.Operators;

public class Arithmetic {
    public static void main(String[] args){
        int a = 10;
        int b = 5;

        // Addition
        System.out.println(a + b);

        //Subtraction
        System.out.println(a - b);

        // Multiplication
        System.out.println(a * b);

        // Division
        System.out.println(a/b);
        System.out.println(a/ (float)b);

        // Modulo Operator
        System.out.println(a % b); // Remainging Value
    }
}
